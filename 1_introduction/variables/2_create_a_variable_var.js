// Prior to the release of ES6, variables were declared with the var keyword.

// A lot of code still uses the var keyword.

/*
General variable naming rules:

- Names cannot start with numbers
- Names are case sensitive and it's bad practice to use variables with the same name in diff cases
- Variable names cannot use keywords or reserved words
*/

var favoriteFood = 'pizza';

var numOfSlices = 8;

console.log(favoriteFood);

console.log(numOfSlices);
