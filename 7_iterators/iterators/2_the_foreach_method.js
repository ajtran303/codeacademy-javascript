// forEach method takes a function as an argument (callback function)
// it passes each array element as an argument to that callback function
// the return value of forEach is always `undefined`
// use arrow function syntax to be cool with ES6

const fruits = ['mango', 'papaya', 'pineapple', 'apple'];

// Iterate over fruits below
fruits.forEach(fruit => console.log(`I want to eat a ${fruit}`));
