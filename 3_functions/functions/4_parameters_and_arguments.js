// When declaring a function, a parameter can be defined that allows specific inputs that will be evaluated when the function is called.

// When the function is called with specific values, those are called arguments. The parameter can be thought of as a "placeholder" for future arguments that will be passed into the function.

function sayThanks(name) {
  console.log('Thank you for your purchase '+ name + '! We appreciate your business.');
}

sayThanks('Cole');
